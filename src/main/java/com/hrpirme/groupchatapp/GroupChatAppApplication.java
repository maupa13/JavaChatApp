package com.hrpirme.groupchatapp;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

/**
 * The main class that starts the Group Chat application.
 */
@SpringBootApplication
public class GroupChatAppApplication {

    /**
     * The main method to start the Group Chat application.
     *
     * @param args The command line arguments passed to the application.
     */
    public static void main(String[] args) {
        SpringApplication.run(GroupChatAppApplication.class, args);
    }
}